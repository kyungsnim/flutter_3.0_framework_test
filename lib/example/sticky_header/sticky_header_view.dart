  }

  _getGrid(BuildContext context) {
    final double leftPadding = 15;
    final double rightPadding = 15;
    final double crossAxisSpacing = 8;

    // if (_viewModel.list.length == 0) {
    //   return _getEmptyArea(context);
    // }

    return SliverPadding(
        padding: EdgeInsets.only(
          left: leftPadding,
          right: rightPadding,
        ),
        sliver: SliverGrid(
          gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
            crossAxisCount: 3, //_viewModel.gridCrossAxisCount,
            mainAxisSpacing: 2, //ResolutionService().getSize(2),
            crossAxisSpacing: crossAxisSpacing,
            childAspectRatio: 0.53, //_viewModel.gridFitType == GridFitType.X2
                // ? 0.53
                // : 0.45,
          ),
          delegate: SliverChildBuilderDelegate(
                (BuildContext context, int idx) {
              /// 사진 비율 3:4 사용
              final photoRatio = (4 / 3);
              final size = MediaQuery.of(context).size;
              final double imageWidth = (size.width - (leftPadding + rightPadding
                  + crossAxisSpacing)) / 3; //_viewModel.gridCrossAxisCount;
              final double imageHeight = imageWidth * photoRatio;

              return Image.network(
                imageList[idx],
                height: imageHeight,
              );
              // return ProductManagementElementView(
              //   _viewModel.list[idx],
              //   imageHeight,
              //   _viewModel.isStatusChangeMode,
              // );
            },
            childCount: imageList.length,
            // childCount: _viewModel.list.length,
          ),
        )
    );
  }
